-- ------------------------------------------------------------
-- SET  - задает новое значение, написать глазированные, там где
-- было с гразурью
-- ------------------------------------------------------------
UPDATE doughnut_rating
SET
type = 'Глазированные'
WHERE type = 'С глазурью';

-- ------------------------------------------------------------
-- Модификаторы можно использовать теже что и при SELECT и DELETE
-- ------------------------------------------------------------
