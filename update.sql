-- Можно использовать оператор AND
-- в конце END можно использовать условие WHERE !!!
-- ------------------------------------------------------------
UPDATE movie_table
SET category =
CASE
    WHEN drama = 'Д' THEN 'драма'
    WHEN comedy = 'Д' THEN 'комедия'
    WHEN action = 'Д' AND rating = 'G' THEN 'боевик'
    ELSE 'разное'
END;